        <h5>Imaging tests</h5>
        <p>Many disorders could cause joint pain. Imaging tests could help pinpoint the source of pain. Options include:</p>

        <div class="row">
          <div class="col-sm-6" data-aos="zoom-in" data-aos-delay="300">
            <div class="txpad">
              <ul>
                <li><p><b>X-rays - </b>They could reveal bone changes which happen in the later stages of avascular necrosis. In the condition's primitive stages, X-rays generally appear normal.</p></li>
                <li><p><b>Magnetic resonance imaging (MRI) & Computed tomography (CT) scan -</b> These tests produce detailed pictures which could show early changes in bone which may indicate avascular necrosis.</p></li>
                <li><p><b>Bone scan - </b>A small amount of radioactive material is administered into your vein. This tracer travels to the portions of your bones which are injured or recovering and shows up as bright spots on the imaging plate.</p></li>
              </ul>
            </div>
          </div>

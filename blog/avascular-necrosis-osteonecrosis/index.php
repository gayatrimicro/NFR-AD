        <div class="section-title" data-aos="fade-up">
          <h2>Avascular Necrosis Causes</h2>          
        </div>
        <div class="padb40">
          <p>Avascular necrosis happens when blood flow to a bone is interrupted or lowered. Lowered blood supply could be caused by:</p>
          <ul>
            <li><p><b>Joint or bone trauma - </b>An injury, like a dislocated joint, may damage nearby blood vessels. Cancer treatments involving radiation also could weaken bone and harm blood vessels.</p></li>
            <li><p><b>Fatty deposits in blood vessels -</b> The fat (lipids) could block small blood vessels, lowering the blood flow which feeds bones.</p></li>
            <li><p><b>Specific conditions - </b>Medical diseases, like sickle cell anemia and Gaucher's disease, also could cause decreased blood flow to bone.</p></li>
          </ul>
          <p>For about 25% of people with avascular necrosis, the cause of disrupted blood flow is undisclosed.</p>
        </div>

        

        <div class="section-title" data-aos="fade-up">
          <h2>Risk factors</h2>                   
        </div>
        <div class="padb40">
          <p>Risk factors for developing avascular necrosis involve:</p> 
          <ul>
            <li><p><b>Trauma - </b>Injuries, like hip dislocation or fracture, could affect nearby blood vessels and lower blood flow to bones.</p></li>
            <li><p><b>Steroid use - </b>Use of high-dose corticosteroids, like prednisone, is a common cause of avascular necrosis. The reason is undisclosed, but one hypothesis is that corticosteroids could increase lipid levels in your blood, lowering blood flow.</p></li>
            <li><p><b>Excessive alcohol use - </b>Consuming numerous alcoholic drinks a day for many years also could cause fatty deposits to form in your blood vessels.</p></li>
            <li><p><b>Bisphosphonate use - </b>Prolonged use of medications to increase bone density may contribute to developing osteonecrosis of the jaw. This rare problem has happened in some people treated with high doses of these medications for cancers, like multiple myeloma and metastatic breast cancer.</p></li>
            <li><p><b>Specific medical treatments - </b>Radiation therapy for cancer could weaken bone. Organ transplantation, particularly kidney transplant, also is related with avascular necrosis.</p></li>
          </ul>
        </div>
        <div class="padb40">
          <p>Medical conditions related with avascular necrosis include:</p> 
          <ul>
            <li><p>Pancreatitis</p></li>
            <li><p>Diabetes</p></li>
            <li><p>Gaucher's disease</p></li>
            <li><p>HIV/AIDS</p></li>
            <li><p>Systemic lupus erythematosus</p></li>
            <li><p>Sickle cell anemia</p></li>
          </ul>
        </div>

        <div class="section-title" data-aos="fade-up">
          <h2>Avascular Necrosis Complications</h2>          
        </div>
        <div class="padb40 text-center">
          <p>Untreated, avascular necrosis is more severe with time. Eventually, the bone could collapse. Avascular necrosis also causes bone to lose its smooth shape, posibbly leading to serious arthritis.</p>
        </div>

        <div class="section-title pad360" data-aos="fade-up">
          <h2>Avascular Necrosis Prevention</h2>
        </div>

        <div class="row">
          <div class="col-lg-6">            
          <p>To lower your risk of avascular necrosis and improve your general health:</p>
          <ul>
            <li><p><b>Limit alcohol – </b>Excess alcohol consumption is one of the top risk factors for developing avascular necrosis.</p></li>
            <li><p><b>Keep cholesterol levels low - </b>Small bits of fat are the most common substance preventing blood supply to bones.</p></li>
            <li><p><b>Monitor steroid use - </b>Make sure your primary care physician knows about your past or present use of high-dose steroids. Steroid associated bone damage appears to worsen with repeated courses of high-dose steroids.</p></li>
            <li><p><b>Quit Smoking – </b>Quit smoking as it puts you at a higher risk.</p></li>
          </ul>

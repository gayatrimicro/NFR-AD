        <div class="section-title mt-5">
          <h2>Avascular Necrosis Treatment</h2>
          <p>The goal is to stop further bone loss.</p>         
        </div>
        <h5 class="mt-5">Medications and therapy</h5>
        <p>In the primitive stages of avascular necrosis, symptoms may be relieved with medication and therapy. Your primary care physician might suggest:</p>
        <ul>
          <li><p><b>Non-steroidal anti-inflammatory drugs - </b>Medicines, for example ibuprofen (Advil, Motrin IB, others) or naproxen sodium (Aleve) may help ease the pain related with avascular necrosis.</p></li>
          <li><p><b>Osteoporosis drugs - </b> Medicines, like alendronate (Fosamax, Binosto), may slow the progression of avascular necrosis, but the evidence is mixed.</p></li>
          <li><p><b>Cholesterol lowering drugs - </b>Lowering the quantity of cholesterol and fat in your blood may help stop the vessel blockages which could cause avascular necrosis.</p></li>
          <li><p><b>Blood thinners - </b>If you have a clotting disorder, blood thinners, like warfarin (Coumadin, Jantoven), may be suggested to stop clots in the vessels feeding your bones.</p></li>
          <li><p><b>Rest - </b>Lowering the weight and stress on your affected bone could slow the damage. You may require to restrict your physical activity or use crutches to keep weight off your joint for many months.</p></li>
          <li><p><b>Exercises - </b>A physical therapist could teach you exercises to help maintain or improve the extent of motion in your joint.</p></li>
          <li><p><b>Electrical stimulation - </b>Electrical currents might encourage your body to grow new bone to replace the affected bone. Electrical stimulation could be used during surgery and applied directly to the affected region. Or it could be administered through electrodes attached to your skin.</p></li>
        </ul>
        <h5 class="mt-5">Surgical and other procedures</h5>
        <p>Because most people do not develop symptoms until avascular necrosis is fairly advanced, your primary care physician may suggest surgery. The options include:</p>
        <ul>
          <li><p><b>Core decompression - </b>The surgeon removes a portion of the inner layer of your bone. In addition to lowering your pain, the extra space within your bone stimulates the generation of healthy bone tissue and new blood vessels.</p></li>
          <li><p><b>Bone transplant or graft - </b>This procedure could help strengthen the region of bone affected by avascular necrosis. The graft is a portion of healthy bone taken from another part of your body.</p></li>
          <li><p><b>Bone reshaping or osteotomy - </b>A portion of bone is removed above or below a weight or load bearing joint, to help shift your weight off the affected bone. Bone reshaping may allow you to delay joint replacement.</p></li>
          <li><p><b>Joint replacement - </b>If your afflicted bone has collapsed or other treatments are not helping, you may require surgery to replace the affected parts of your joint with plastic or metal parts.</p></li>
          <li><p><b>Regenerative medicine treatment - </b>Bone marrow aspirate and concentration is a newer procedure which might be appropriate for primitive stage avascular necrosis of the hip. Stem cells are harvested or collected from your bone marrow. During surgery, a core of dead hip bone is removed and stem cells inserted in its place, possibly allowing for growth of new bone. More study is required.</p></li>
        </ul>

          <h2>Osteochondritis Dissecans Causes</h2>
          <p>The cause of osteochondritis dissecans is unrecognized. The lowered blood flow to the end of the affected bone may result from repetitive trauma — small, multiple episodes of minor, unrecognized injury which damage the bone. There may be a genetic component, making some people more inclined to develop the disorder.</p>

        <div class="section-title mt-5">
          <h2>Osteochondritis Dissecans Treatment</h2>          
        </div>
        <p>Treatment of osteochondritis dissecans is intended to restore the normal functioning of the damaged joint and ease pain, as well as lower the risk of osteoarthritis. None of the treatments work for everyone. In children whose bones are still growing, the bone defect might heal with a period of rest and protection.</p>
        <h5 class="mt-5">Therapy</h5>
        <p>Primarily, your primary care physician will likely suggest conservative measures, which may include:</p>
        <ul>
          <li><p><b>Resting your joint - </b>Avoid activities which stress your joint, like jumping and running if your knee is damaged. You may require to use crutches for a time, particularly if pain causes you to limp. Your primary care physician may also recommend wearing a splint, cast or brace to immobilize the joint for a few weeks.</p></li>
          <li><p><b>Physical therapy - </b>Most usually, this therapy includes stretching, range-of-motion exercises and strengthening exercises for the muscles which support the involved joint. Physical therapy is commonly suggested after surgery, as well.</p></li>
        </ul>
        <h5 class="mt-5">Osteochondritis Dissecans Surgery</h5>
        If you have a loose portion in your joint, if the damaged area is still present after your bones have stopped growing, or if conservative treatments do not help after 4 to 6 months, you may require surgery. The type of surgery will depend upon the size and stage of the injury and how mature your bones are.

                <li><p><b>Magnetic resonance imaging (MRI) - </b>Using radio waves and a strong magnetic field, an magnetic resonance imaging (MRI) could offer detailed pictures of both hard and soft tissues, including the bone and cartilage. If X-rays appear normal but you still have symptoms, your primary care physician may order a magnetic resonance imaging (MRI).</p></li>
                <li><p><b>Computerized tomography (CT) scan - </b>This technique combines X-ray offers taken from different angles to produce cross-sectional pictures of internal structures. Computerized tomography (CT) scans permit your primary care physician to see bone in high detail, which could help pinpoint the location of loose portions within the joint.</p></li>

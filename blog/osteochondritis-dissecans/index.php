            <div class="box">
              <p><a href="https://nowfindrelief.com/knee-pain-clinic/"><b>Osteochondritis dissecans</b></a> is a joint disease in which bone underneath the cartilage of a joint dies because of lack of blood flow. This bone and cartilage could then break loose, causing pain and possibly obstructing joint motion.</p>
              <p>Osteochondritis dissecans happen most usually in children and adolescents. It could cause symptoms either after an injury to a joint or after many months of activity, particularly high-impact activity like jumping and running, which affects the joint. The condition happens most commonly in the knee, but also happens in elbows, ankles and other joints.</p>
              <p>Primary care physicians stage osteochondritis dissecans according to the size of the injury, whether the portion is partially or completely detached, and whether the portion stays in place. If the loosened piece of cartilage and bone stays in place, you might have few or no symptoms. For young children whose bones are still developing, the injury may heal by itself.</p>
              <p>Surgery may be required if the portion comes loose and gets caught between the moving parts of your joint or if you have continuous pain.</p>

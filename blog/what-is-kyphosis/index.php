        <div class="section-title" data-aos="fade-up">
          <h2>Kyphosis Causes</h2>          
        </div>
        <p>The individual bones (vertebrae) that create a healthy spine look similar to cylinders stacked in a column. Kyphosis happens when the vertebrae in the upper back become more spear-shaped.</p>
        <p>Abnormal vertebrae could be caused by:</p>
        <ul>
          <li><p><b>Fractures - </b>Broken or crushed vertebrae (compression fractures) could result in curvature of the spine. Mild compression fractures usually do not produce noticeable signs or symptoms.</p></li>
          <li><p><b>Osteoporosis - </b>This bone-thinning disorder could cause spinal curvature, particularly if weakened vertebrae result in compression fractures. Osteoporosis is most frequent in older women and people who have taken corticosteroids for long periods of time.</p></li>
          <li><p><b>Disc degeneration - </b>Soft, circular discs serve as cushions between spinal vertebrae. With age, these discs dry out and shrink, which usually worsens kyphosis.</p></li>
          <li><p><b>Scheuermann's disease - </b>Also known as Scheuermann's kyphosis, this disease generally starts during the growth spurt that happens before puberty. Boys are affected more usually than girls.</p></li>
          <li><p><b>Birth defects -</b>Spinal bones that do not develop properly before birth could cause kyphosis.</p></li>
          <li><p><b>Syndromes -</b> Kyphosis in children could also be related to specific syndromes, for example, Ehlers-Danlos syndrome and Marfan syndrome.</p></li>
          <li><p><b>Cancer and cancer treatments - </b>Cancer in the spine could weaken vertebrae and make them more vulnerable to compression fractures, as could chemotherapy and radiation cancer treatments.</p></li>
        </ul>

        <div class="section-title mart100" data-aos="fade-up">
          <h2>Kyphosis Complications</h2>
        </div>
        <p>In addition to causing back pain, kyphosis might cause:</p>
        <ul>
          <li><p><b>Breathing problems - </b>Serious kyphosis could put pressure on the lungs.</p></li>
          <li><p><b>Limited physical functions - </b>Kyphosis is related to weakened back muscles and difficulty doing tasks for example walking and getting out of chairs. The spinal curvature could also make it difficult to gaze upward or drive and could cause pain when you lie down.</p></li>
          <li><p><b>Digestive problems - </b>Serious kyphosis could compress the digestive tract, causing problems for example acid reflux and difficulty with swallowing.</p></li>
          <li><p><b>Body image problems -</b> People with kyphosis, particularly adolescents, might develop a poor body image from having a rounded back or from wearing a brace to correct the condition. For older people, poor body image could lead to social isolation.</p></li>
        </ul>

        <div class="section-title mart100" data-aos="fade-up">
          <h2>Kyphosis Diagnosis</h2>          
        </div>
        <p>Your doctor or primary care physician will generally conduct a thorough physical exam, including checking your height. You might be asked to bend forward from the waist while your primary care physician views your spine from the side. Your primary care physician may also perform a neurological examination to check your reflexes and muscle strength.</p>
        <p><b>After assessing your signs and symptoms, your primary care physician might suggest:</b></p>
        <ul>
          <li><p><b>X-rays or Computed tomography (CT) scans -</b> X-rays could determine the degree of curvature and detect defects of the vertebrae. A computed tomography (CT) scan may be suggested if your primary care physician wants more-detailed pictures.</p></li>
          <li><p><b>Magnetic resonance imaging (MRI) - </b>These images could detect infection or a tumor in your spine.
</p></li>
          <li><p><b>Nerve tests - </b>If you are experiencing numbness or muscle weakness, your primary care physician might suggest tests to determine how well nerve impulses are traveling between your spinal cord and your extremities.</p></li>
          <li><p><b>Bone density tests -</b> Low-density bone could worsen kyphosis.</p></li>
        </ul>

        <div class="section-title pad360 mart100" data-aos="fade-up">
          <h2>Kyphosis Treatment</h2>
        </div>       

        <div class="row">
          <div class="image col-lg-6" data-aos="zoom-in" data-aos-delay="100">
            <img src="../../assets/img/blog/k1.jpg" alt="img" class="img-fluid">
          </div>
          <div class="image col-lg-6">
            <p>Kyphosis treatment is determined by the cause and seriousness of your condition.</p>
            <p><b>Medications</b></p>
            <p>Your primary care physician may recommend medication, including:</p>
            <ul>
              <li><p><b>Pain relievers - </b>If over-the-counter medicines — for example, acetaminophen (Tylenol, others), ibuprofen (Advil, Motrin IB, others), or naproxen sodium (Aleve) — are not enough, stronger pain medications are available by prescription.</p></li>
              <li><p><b>Osteoporosis medications - </b>bone-strengthening medications might help stop additional spinal fractures that would worsen your kyphosis.</p></li>
            </ul>
            <br>
          </div>
        </div>

        
        <br><br>
        <p><b>Therapy</b></p>
        <p>Therapy could help manage specific types of kyphosis. Your primary care physician may suggest:</p>
        <ul>
          <li><p><b>Exercises - </b>Stretching exercises might help improve spinal flexibility and ease back pain.</p></li>
          <li><p><b>Bracing - </b>Children who have Scheuermann's disease might be able to prevent the progression of kyphosis by wearing a body brace while their bones are still growing.</p></li>
        </ul><br>

        <div class="row">
          <div class="col-lg-6"> 
          <p><b>Surgical and other procedures</b></p>           
          <p class="mt-5 mart0360">Surgery may be suggested for serious kyphosis that is pinching the spinal cord or nerve roots. Spinal fusion is the most common procedure for lowering the degree of curvature. The surgeon inserts pieces of bone between the vertebrae and then connects the vertebrae together with metal rods and screws until the spine heals together in a corrected position.</p>
          </div>
